
# Q-13: Explain Exception handling? What is an Error in Python?

# ans:
# Exception handling:

# When an error occurs, or exception as we call it, Python will normally stop and generate an error message.
# Exception handling is the process of responding to unwanted or
# unexpected events when a computer program runs.
# Exception handling deals with these events to avoid the program or system crashing, and without this process,
# exceptions would disrupt the normal operation of a program.
# Exception Handling is a mechanism to handle runtime errors such as
# ClassNotFoundException, IOException, SQLException, RemoteException, etc.

# an Error in Python:
# The try block lets you test a block of code for errors.
# The except block lets you handle the error.
# The finally block lets you execute code, regardless of the result of the try- and except blocks.